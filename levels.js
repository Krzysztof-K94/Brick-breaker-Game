import Brick from './brick.js';

export const buildLevel = (game, level) => {
    let bricks = [];

    level.forEach((row, rowIndex) => {
        row.forEach((brick, brickIndex) => {
            //brick(number) will define type of a brick
            if(brick) bricks.push(new Brick(game, {x: 102.4 * brickIndex, y: 40 * rowIndex}, brick));
        });
    });

    return bricks;
};

